package day19_ForLoop;

public class LoopsTasks {
    public static void main(String[] args) {

        for(int i = 1000; i <= 2000; i++){
            System.out.println("Even devisible: "+i+" numbers ");
        }
        System.out.println();
        System.out.println("=============================");


        for(int t = 99; t <= 100; t++){
            System.out.println(t);
        }
        System.out.println();
        System.out.println("=========================");
        for(char i='a';i<='z';i++) {
            System.out.println(i + " ");


        }
    }
}
