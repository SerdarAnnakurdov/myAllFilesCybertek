package day11_MultiBranchIf;

public class PasitiveNagitiveZero {
    public static void main(String[] args) {
        int number = -98;

        if(number <0){
            System.out.println("Negative");
        }
        if(number > 0){
            System.out.println("Positive");
        }
        if(number == 0 ){
            System.out.println("zero");
        }
        System.out.println("------------------------------");

        if(number<0){
            System.out.println("Negative");
        }else if(number > 0){
            System.out.println("positive");
        }






    }
}
