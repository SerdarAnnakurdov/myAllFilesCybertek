package day09_LogicalOperators;

public class OddTasks {
    public static void main(String[] args) {
        int num = 101;
        boolean even = num % 2 == 0;


        if(even){
            System.out.println(num+"even number");
        }
        if(!even){
            System.out.println(num+" is odd number");
        }





    }
}
