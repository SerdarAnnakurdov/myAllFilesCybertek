package day03_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello everyone");
        System.out.println("My name is BLABLABLA, I am from Virginia ");

        System.out.println("==========================");

        System.out.println("Hello everyone\nMy name is Your name and I am from Virginia");
        System.out.println("Everyone have to come to night\nTo my apartment which is on 20th ave");


        System.out.println("=======================");


        System.out.println("\tToday I learned Java");
        System.out.println("//"); //pring single frontslash
        System.out.println("\tIam Boss");
        System.out.println("\\");   //print single backslash

        System.out.println("=========================");
        System.out.println("My name is 'Serdar'");
        System.out.println("My name is \'Serdar\'");
        System.out.println("My name is \"serdar\"");



    }




}


